import turtle


# STEP1: Lets draw 1 circle  

# Create the screen
screen = turtle.Screen()
screen.bgcolor("black")

t = turtle.Turtle()
t.color("blue")
t.speed(1)
t.pensize(2)

# Draw overlapping circles with both turtles
t.circle(50)


# Keep the window open
turtle.done()

