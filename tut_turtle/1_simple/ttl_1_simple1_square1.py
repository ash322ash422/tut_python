import turtle

# Here we will draw a square by following series of steps

# STEP1: Lets draw 1st line left-> right using turtle
 
# Set up the turtle
t = turtle.Turtle()  # Create a turtle object
turtle.bgcolor("lightblue")  # Set background color
t.pencolor("green")  # Set pen color 
t.pensize(3)  # Set pen thickness


t.forward(100)       # Move forward 100 pixels 
t.right(90)          # Turn right 90 degrees

# Keep the window open
turtle.done() 

